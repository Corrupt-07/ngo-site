    <div class="container">
        <h1 class="text-center">Youth empowerment </h1>
        <p>Children are the future of a nation. They are the best change agents, be it in the family or the community in
            which they live. It is therefore crucial to help the children, catch them young and inculcate in them
            feelings of empathy and conscience so that they grow up as responsible individuals. Sensing this need, Smile
            Foundation came up with Child For Child (CFC) programme in 2006. Sensitization of privileged children and
            their parents, towards the existing inequalities around them, is an important objective of CFC.
        </p>
        <p>Privileged children are sensitized about the deprivation and pain endured by the underprivileged kids. CFC
            seeks to inculcate a conscience and value system in the children so that they grow up to become responsible
            citizens and change makers. Before their minds are set with age, the CFC programme tries to make them count
            their own blessings and understand the plight of less privileged ones. Once they start realizing the worth
            of the privileges they are born with, they automatically turn their thoughts towards positivity and develop
            the right outlook. This eventually helps them develop into not only successful but responsible individuals
            in life. They grow up to become significant change makers, who contribute positively to the society.</p>
